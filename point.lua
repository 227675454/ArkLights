rotate_point = function(x)
  local d = point_delimeter
  if type(x) ~= 'string' or not x:find(d) then return x end
  return x:gsub('([^' .. d .. ']+)' .. d .. '(.+)', '%2' .. d .. '%1')
end
expand_fight = function()

  local iter
  iter = {'A', 'B', 'C', 'D'}
  for i = 2, 2 do
    for _, x in pairs(iter) do
      target['PR-' .. x .. '-' .. i] = rotate_point(
                                         target['PR-' .. x .. '-' .. (i - 1)])
    end
  end
  for i = 2, 5 do target['AP-' .. i] = rotate_point(target['AP-' .. (i - 1)]) end
  iter = {'CE', 'CA', 'LS', 'SK'}
  for _, v in pairs(iter) do
    for i = 1, 5 do target[v .. '-' .. i] = target['AP-' .. i] end
  end
end
